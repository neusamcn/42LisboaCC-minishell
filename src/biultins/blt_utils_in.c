bool export_argv(char c, int j)
{
    if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
        c == '_' || (j > 0 && c >= '0' && c <= '9') || c == '=')
        return (1);
    return (0);
}